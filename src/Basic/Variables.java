package Basic;

public class Variables {
    /*
    Define variables for different Data Types int,
    Boolean, char, float, double and print on the Console
    * */
    public static void main(String[] args) {
        System.out.println("this program to print variables in java ");
        int rollNo = 10;
        boolean isStudent = true;
        char firstCharacter = 'A';
        float pi = 3.14f;
        double profit = 34.98675;
        System.out.println("rollNo "+rollNo);
        System.out.println("is Student "+isStudent);
        System.out.println("first character "+firstCharacter);
        System.out.println("pi "+pi);
        System.out.println("profit "+profit);
    }
}
